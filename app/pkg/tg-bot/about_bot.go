	// —Å–æ–æ–±—â–µ–Ω–∏–µ –ø—Ä–∏–≤–µ—Ç—Å—Ç–≤–∏—è
	welcomeMessage := "–î–æ–±—Ä–æ –ø–æ–∂–∞–ª–æ–≤–∞—Ç—å –≤ *FINLY üêô*!\n\n–ù–∞–∂–º–∏—Ç–µ *‚ÑπÔ∏è –ò–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è* –≤ –º–µ–Ω—é, —á—Ç–æ–±—ã —É–∑–Ω–∞—Ç—å –±–æ–ª—å—à–µ –æ –º–æ–∏—Ö –≤–æ–∑–º–æ–∂–Ω–æ—Å—Ç—è—Ö.\n\n–í–ø–µ—Ä—ë–¥ –∫ —Ñ–∏–Ω–∞–Ω—Å–æ–≤–æ–º—É —É—Å–ø–µ—Ö—É üöÄ"
